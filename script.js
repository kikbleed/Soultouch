const state = { cart: JSON.parse(localStorage.getItem('stm-cart-ok') || '[]') };
const money = n => 'R' + Number(n).toLocaleString('en-ZA');
const img = p => `assets/images/${p.img}`;
const img2 = p => `assets/images/${p.img2 || p.img}`;
function card(p){
  return `<article class="product-card" data-brand="${p.brand}" data-name="${p.name.toLowerCase()}">
    <div class="media shoe-hover">
      <span class="badge ${p.tag==='Sale'?'sale':''}">${p.tag}</span>
      <img class="product-image primary" src="${img(p)}" alt="${p.name}">
      <img class="product-image secondary" src="${img2(p)}" alt="${p.name} alternate view" loading="lazy">
      <button class="quick-buy" type="button" data-quick="${p.id}">Quick buy</button>
    </div>
    <div class="product-info"><div class="product-brand">${p.brand}</div><div class="product-name">${p.name}</div><div class="product-price">${money(p.price)} ${p.compare?`<span class="compare">${money(p.compare)}</span>`:''}</div><div class="product-meta">Sizes UK ${p.sizes.slice(0,5).join(' / ')}${p.sizes.length>5?' +':''}</div></div>
  </article>`;
}
function getFilteredProducts(row){
  let list = products.filter(p=>p.row.includes(row));
  if(row==='all'){
    const brand = document.querySelector('[data-brand-filter]')?.value || 'all';
    const search = (document.querySelector('[data-search]')?.value || '').toLowerCase();
    const sort = document.querySelector('[data-sort]')?.value || 'featured';
    if(brand !== 'all') list = list.filter(p=>p.brand===brand);
    if(search) list = list.filter(p=>(p.name+p.brand+p.tag+p.sizes.join(' ')).toLowerCase().includes(search));
    if(sort==='low') list.sort((a,b)=>a.price-b.price);
    if(sort==='high') list.sort((a,b)=>b.price-a.price);
    if(sort==='az') list.sort((a,b)=>a.name.localeCompare(b.name));
  }
  return list;
}
function renderRows(){
  document.querySelectorAll('[data-row]').forEach(el=>{
    const row = el.dataset.row;
    const list = getFilteredProducts(row);
    el.innerHTML = list.map(card).join('') || '<p class="empty-row">No sneakers found.</p>';
  });
  bindQuick();
}
function bindQuick(){document.querySelectorAll('[data-quick]').forEach(btn=>btn.onclick=()=>openQuick(products.find(p=>p.id==btn.dataset.quick)));}
function openQuick(p){
  const modal=document.querySelector('[data-modal]'), content=document.querySelector('[data-modal-content]');
  content.innerHTML = `<div class="quick-view"><div class="qv-media"><img src="${img(p)}" alt="${p.name}"></div><div class="qv-info"><div class="product-brand">${p.brand}</div><h2>${p.name}</h2><div class="product-price">${money(p.price)} ${p.compare?`<span class="compare">${money(p.compare)}</span>`:''}</div><p>Premium Soultouch Movement sneaker selection. Choose your UK size and add to cart.</p><div class="sizes">${p.sizes.map((s,i)=>`<button class="size-btn ${i===0?'active':''}" data-size="UK ${s}">${s}</button>`).join('')}</div><button class="btn btn-dark" data-add="${p.id}">Add to cart</button></div></div>`;
  modal.classList.add('open'); modal.setAttribute('aria-hidden','false'); document.querySelector('[data-scrim]').classList.add('open');
  content.querySelectorAll('.size-btn').forEach(b=>b.onclick=()=>{content.querySelectorAll('.size-btn').forEach(x=>x.classList.remove('active'));b.classList.add('active')});
  content.querySelector('[data-add]').onclick=()=>{addToCart(p.id, content.querySelector('.size-btn.active').dataset.size); closeModal();};
}
function addToCart(id,size='UK 8'){
  const item = state.cart.find(x=>x.id===id && x.size===size);
  if(item) item.qty++; else state.cart.push({id,size,qty:1});
  saveCart(); toast('Sneaker added to cart');
}
function saveCart(){localStorage.setItem('stm-cart-ok', JSON.stringify(state.cart));renderCart();}
function renderCart(){
  const count = state.cart.reduce((a,b)=>a+b.qty,0);
  document.querySelectorAll('[data-cart-count],[data-cart-count-mobile]').forEach(e=>e.textContent=count);
  const wrap=document.querySelector('[data-cart-items]');
  if(!wrap) return;
  if(!state.cart.length){wrap.innerHTML='<p>Your cart is empty.</p>'} else {
    wrap.innerHTML=state.cart.map((i,idx)=>{const p=products.find(x=>x.id===i.id);return `<div class="cart-line"><img src="${img(p)}" alt="${p.name}"><div><h4>${p.name}</h4><small>${i.size} × ${i.qty}</small><br><strong>${money(p.price*i.qty)}</strong></div><button data-remove="${idx}">Remove</button></div>`}).join('');
  }
  const subtotal = state.cart.reduce((sum,i)=>{const p=products.find(x=>x.id===i.id);return sum+p.price*i.qty},0);
  document.querySelector('[data-subtotal]').textContent=money(subtotal);
  document.querySelectorAll('[data-remove]').forEach(b=>b.onclick=()=>{state.cart.splice(+b.dataset.remove,1);saveCart()});
}
function openCart(){document.querySelector('[data-cart-drawer]').classList.add('open');document.querySelector('[data-scrim]').classList.add('open')}
function closeDrawers(){document.querySelectorAll('.cart-drawer,.search-drawer').forEach(d=>d.classList.remove('open'));document.querySelector('[data-scrim]').classList.remove('open')}
function closeModal(){document.querySelector('[data-modal]').classList.remove('open');document.querySelector('[data-scrim]').classList.remove('open')}
function toast(msg){const t=document.querySelector('[data-toast]');t.textContent=msg;t.classList.add('show');setTimeout(()=>t.classList.remove('show'),1800)}
function renderSearch(q){const results=document.querySelector('[data-search-results]');q=q.toLowerCase();const list=products.filter(p=>(p.name+p.brand+p.tag).toLowerCase().includes(q)).slice(0,30);results.innerHTML=list.map(p=>`<button class="search-result" type="button" data-quick="${p.id}"><img src="${img(p)}" alt="${p.name}"><div><strong>${p.name}</strong><br><span>${p.brand} • ${money(p.price)}</span></div></button>`).join('') || '<p>No sneakers found.</p>';bindQuick()}
function scrollRow(row, direction){
  const el=document.querySelector(`[data-row="${row}"]`);
  if(!el) return;
  const amount=Math.max(280, Math.round(el.clientWidth*.85));
  el.scrollBy({left: direction==='next'?amount:-amount, behavior:'smooth'});
}
document.addEventListener('DOMContentLoaded',()=>{
  renderRows(); renderCart();
  document.querySelectorAll('[data-search],[data-brand-filter],[data-sort]').forEach(e=>e.addEventListener('input',renderRows));
  document.querySelectorAll('[data-brand-jump]').forEach(a=>a.addEventListener('click',()=>setTimeout(()=>{document.querySelector('[data-brand-filter]').value=a.dataset.brandJump;renderRows()},50)));
  document.querySelectorAll('[data-cart-toggle]').forEach(b=>b.onclick=openCart);
  document.querySelector('[data-cart-close]').onclick=closeDrawers;
  document.querySelector('[data-scrim]').onclick=()=>{closeDrawers();closeModal()};
  document.querySelector('[data-modal-close]').onclick=closeModal;
  document.querySelector('[data-menu-toggle]').onclick=()=>document.querySelector('[data-nav]').classList.toggle('open');
  document.querySelector('[data-search-toggle]').onclick=()=>{document.querySelector('[data-search-drawer]').classList.add('open');document.querySelector('[data-scrim]').classList.add('open');document.querySelector('[data-global-search]').focus();renderSearch('')};
  document.querySelector('[data-search-close]').onclick=closeDrawers;
  document.querySelector('[data-global-search]').addEventListener('input',e=>renderSearch(e.target.value));
  document.querySelector('[data-checkout]').onclick=()=>toast('Checkout is demo-ready. Connect payment gateway before launch.');
  document.querySelectorAll('[data-scroll-row]').forEach(btn=>btn.onclick=()=>scrollRow(btn.dataset.scrollRow, btn.dataset.direction));
});
